/*Find the names of aircraft such that all pilots certified to operate them have salaries more than Rs.80,000*/
SELECT DISTINCT A.A_NAME FROM AIRCRAFT A,EMPLOYEE E,CERTIFIED C WHERE E.SALARY>80000 AND A.A_ID=C.A_ID AND E.E_ID=C.E_ID;

/*For each pilot who is certified for more than three aircrafts, find the eid and the maximum cruisingrange of the aircraft for which she or he is certified*/
SELECT E.E_ID,MAX(A.CRUISING_RANGE) FROM EMPLOYEE E,CERTIFIED C,AIRCRAFT A WHERE E.E_ID IN (SELECT E_ID FROM CERTIFIED GROUP BY E_ID HAVING COUNT(*)>3) AND A.A_ID=C.A_ID AND E.E_ID=C.E_ID GROUP BY E.E_ID;

/*Find the names of pilots whose salary is less than the price of the cheapest route from Bengaluru to Frankfurt.*/
SELECT DISTINCT E.E_NAME FROM EMPLOYEE E,CERTIFIED C WHERE E.E_ID=C.E_ID AND E.SALARY<(SELECT MIN(PRICE) FROM FLIGHTS WHERE TO_PLACE="FRANKFURT" AND FROM_PLACE="BENGALURU");

/*For all aircraft with cruisingrange over 1000 Kms, find the name of the aircraft and the average salary of all pilots certified for this aircraft*/
SELECT A.A_NAME,AVG(E.SALARY) FROM AIRCRAFT A,CERTIFIED C,EMPLOYEE E WHERE C.E_ID=E.E_ID AND C.A_ID=A.A_ID AND A.A_ID IN(SELECT A_ID FROM AIRCRAFT WHERE CRUISING_RANGE>1000) GROUP BY A.A_ID;

/*Find the names of pilots certified for some Boeing aircraft*/
SELECT DISTINCT E.E_NAME FROM EMPLOYEE E,AIRCRAFT A,CERTIFIED C WHERE E.E_ID=C.E_ID AND A.A_ID=C.A_ID AND A.A_ID IN (SELECT A_ID FROM AIRCRAFT WHERE A_NAME LIKE '%AIR%');

